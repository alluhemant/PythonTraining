"""Type Conversion
Here will be converting one data type to
another data type.
We perform this operation using our built-in functions.
"""

number_String = "123"
print(type(number_String))
number_int = int(number_String)
print(type(number_int))

number_float = 3.142
print(type(number_float))
number_integer = int(number_float)
print(number_integer)
print(type(number_integer))

number_int1 = 6362
print(type(number_int1))
string = str(number_int1)
print(string)
print(type(string))
